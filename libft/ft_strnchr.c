#include "libft.h"

char	*ft_strnchr(const char *s, int c, int n)
{
	int	i;

	i = 0;
	while (*s != (char)c && *s != '\0' && i < n)
	{
		s++;
		i++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (NULL);
}
